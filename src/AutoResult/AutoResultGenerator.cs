            var result = await resultTask.ConfigureAwait(false);
            return result.Bind(binder);
        }

        public static async System.Threading.Tasks.Task<TOut> MatchAsync<T, TOut>(
            this System.Threading.Tasks.Task<Result<T>> resultTask,
            Func<T, TOut> onSuccess,
            Func<Exception, TOut> onFailure)
        {
            var result = await resultTask.ConfigureAwait(false);
            return result.Match(onSuccess, onFailure);
        }
    }

    /// <summary>
    /// Place on a <c>partial</c> class to generate <c>Try*()</c> wrappers for all
    /// public methods — sync, async, and void-returning are all handled.
    /// </summary>
    [AttributeUsage(AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
    public sealed class TryWrapAttribute : Attribute { }
}
";

    // ── Diagnostics ─────────────────────────────────────────────────────────
    private static readonly DiagnosticDescriptor AR001 = new(
        id: "AR001",
        title: "[TryWrap] requires a partial class",
        messageFormat: "'{0}' has [TryWrap] but is not declared as partial. Add the 'partial' modifier to enable Try*() method generation.",
        category: "AutoResult",
        defaultSeverity: DiagnosticSeverity.Error,
        isEnabledByDefault: true);

    private static readonly DiagnosticDescriptor AR002 = new(
        id: "AR002",
        title: "No wrappable methods found",
        messageFormat: "'{0}' has [TryWrap] but has no public instance methods to wrap. Add public methods to generate Try*() wrappers.",
        category: "AutoResult",
        defaultSeverity: DiagnosticSeverity.Warning,
        isEnabledByDefault: true);

    // ── Data models ──────────────────────────────────────────────────────────
    private sealed class MethodInfo
    {
        public string Name         { get; set; } = "";
        public string ReturnKind   { get; set; } = ""; // "sync" | "async" | "void" | "async-void"
        public string ReturnTypeFqn { get; set; } = ""; // inner T (for Result<T>), or "Unit"
        public string ParamList    { get; set; } = ""; // "int id, string name"
        public string ArgList      { get; set; } = ""; // "id, name"
    }

    private sealed class WrapperInfo
    {
        public string ClassFqn          { get; set; } = "";
        public string ClassNamespace     { get; set; } = "";
        public string ClassName          { get; set; } = "";
        public bool   IsPartial          { get; set; }
        public bool   HasMethods         { get; set; }
        public List<MethodInfo> Methods  { get; set; } = new();
    }

    // ── Initialize ───────────────────────────────────────────────────────────
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // Always emit the core types
        context.RegisterPostInitializationOutput(ctx =>
            ctx.AddSource("AutoResult.Core.g.cs", CoreTypesSource));

        // Find [TryWrap] classes
        var wrappers = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                "AutoResult.TryWrapAttribute",
                predicate: static (n, _) => n is ClassDeclarationSyntax,
                transform: TransformClass)
            .Where(x => x is not null)
            .Select((x, _) => x!);

        context.RegisterSourceOutput(wrappers, GenerateWrappers);
    }

    // ── Transform ────────────────────────────────────────────────────────────
    private static WrapperInfo? TransformClass(
        GeneratorAttributeSyntaxContext ctx,
        CancellationToken ct)
    {
        if (ctx.TargetSymbol is not INamedTypeSymbol typeSymbol) return null;

        var typeDecl = (ClassDeclarationSyntax)ctx.TargetNode;
        bool isPartial = typeDecl.Modifiers.Any(SyntaxKind.PartialKeyword);

        var methods = new List<MethodInfo>();

        if (isPartial)
        {
            foreach (var member in typeSymbol.GetMembers())
            {
                ct.ThrowIfCancellationRequested();
                if (member is not IMethodSymbol method) continue;
                if (method.IsStatic || method.DeclaredAccessibility != Accessibility.Public) continue;
                if (method.MethodKind != MethodKind.Ordinary) continue;
                if (method.Name.StartsWith("Try")) continue; // don't wrap already-wrapped methods

                var info = BuildMethodInfo(method, ctx.SemanticModel.Compilation);
                if (info != null) methods.Add(info);
            }
        }

        var ns = typeSymbol.ContainingNamespace?.ToDisplayString() ?? "";

        return new WrapperInfo
        {
            ClassFqn       = typeSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
            ClassNamespace  = ns,
            ClassName       = typeSymbol.Name,
            IsPartial       = isPartial,
            HasMethods      = methods.Count > 0,
            Methods         = methods
        };
    }

    private static MethodInfo? BuildMethodInfo(IMethodSymbol method, Compilation compilation)
    {
        var returnType = method.ReturnType;
        string kind;
        string innerFqn;

        // Detect Task<T>, Task, T or void
        if (IsTaskOf(returnType, out var taskArg))
        {
            if (taskArg == null)
            {
                kind     = "async-void"; // Task (no result)
                innerFqn = "global::AutoResult.Unit";
            }
            else
            {
                kind     = "async";
                innerFqn = taskArg.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
            }
        }
        else if (returnType.SpecialType == SpecialType.System_Void)
        {
            kind     = "void";
            innerFqn = "global::AutoResult.Unit";
        }
        else
        {
            kind     = "sync";
            innerFqn = returnType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        }

        var paramList = string.Join(", ", method.Parameters.Select(p =>
            $"{p.Type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)} {p.Name}"));
        var argList = string.Join(", ", method.Parameters.Select(p => p.Name));

        return new MethodInfo
        {
            Name          = method.Name,
            ReturnKind    = kind,
            ReturnTypeFqn = innerFqn,
            ParamList     = paramList,
            ArgList       = argList
        };
    }

    // ── Code generation ──────────────────────────────────────────────────────
    private static void GenerateWrappers(SourceProductionContext ctx, WrapperInfo info)
    {
        if (!info.IsPartial)
        {
            ctx.ReportDiagnostic(Diagnostic.Create(AR001, Location.None, info.ClassName));
            return;
        }

        if (!info.HasMethods)
        {
            ctx.ReportDiagnostic(Diagnostic.Create(AR002, Location.None, info.ClassName));
            return;
        }

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated by AutoResult.Generator/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using AutoResult;");
        sb.AppendLine();

        bool hasNs = !string.IsNullOrEmpty(info.ClassNamespace);
        if (hasNs)
        {
            sb.AppendLine($"namespace {info.ClassNamespace}");
            sb.AppendLine("{");
        }

        sb.AppendLine($"    public partial class {info.ClassName}");
        sb.AppendLine("    {");

        foreach (var m in info.Methods)
        {
            string tryName  = "Try" + m.Name;
            string paramSep = m.ParamList.Length > 0 ? ", " : "";
            string callArgs = m.ArgList;

            switch (m.ReturnKind)
            {
                case "sync":
                    sb.AppendLine($"        /// <summary>Wraps <see cref=\"{m.Name}\"/> and returns a <see cref=\"Result{{T}}\"/> instead of throwing.</summary>");
                    sb.AppendLine($"        public Result<{m.ReturnTypeFqn}> {tryName}({m.ParamList})");
                    sb.AppendLine("        {");
                    sb.AppendLine("            try");
                    sb.AppendLine($"                {{ return Result<{m.ReturnTypeFqn}>.Ok({m.Name}({callArgs})); }}");
                    sb.AppendLine("            catch (Exception ex)");
                    sb.AppendLine($"                {{ return Result<{m.ReturnTypeFqn}>.Fail(ex); }}");
                    sb.AppendLine("        }");
                    break;

                case "void":
                    sb.AppendLine($"        /// <summary>Wraps <see cref=\"{m.Name}\"/> and returns a <see cref=\"Result{{T}}\"/> instead of throwing.</summary>");
                    sb.AppendLine($"        public Result<Unit> {tryName}({m.ParamList})");
                    sb.AppendLine("        {");
                    sb.AppendLine("            try");
                    sb.AppendLine($"            {{ {m.Name}({callArgs}); return Result<Unit>.Ok(Unit.Value); }}");
                    sb.AppendLine("            catch (Exception ex)");
                    sb.AppendLine("            { return Result<Unit>.Fail(ex); }");
                    sb.AppendLine("        }");
                    break;

                case "async":
                    sb.AppendLine($"        /// <summary>Wraps <see cref=\"{m.Name}\"/> and returns a <see cref=\"Result{{T}}\"/> instead of throwing.</summary>");
                    sb.AppendLine($"        public async Task<Result<{m.ReturnTypeFqn}>> {tryName}({m.ParamList})");
                    sb.AppendLine("        {");
                    sb.AppendLine("            try");
                    sb.AppendLine($"                {{ return Result<{m.ReturnTypeFqn}>.Ok(await {m.Name}({callArgs}).ConfigureAwait(false)); }}");
                    sb.AppendLine("            catch (Exception ex)");
                    sb.AppendLine($"                {{ return Result<{m.ReturnTypeFqn}>.Fail(ex); }}");
                    sb.AppendLine("        }");
                    break;

                case "async-void":
                    sb.AppendLine($"        /// <summary>Wraps <see cref=\"{m.Name}\"/> and returns a <see cref=\"Result{{T}}\"/> instead of throwing.</summary>");
                    sb.AppendLine($"        public async Task<Result<Unit>> {tryName}({m.ParamList})");
                    sb.AppendLine("        {");
                    sb.AppendLine("            try");
                    sb.AppendLine($"            {{ await {m.Name}({callArgs}).ConfigureAwait(false); return Result<Unit>.Ok(Unit.Value); }}");
                    sb.AppendLine("            catch (Exception ex)");
                    sb.AppendLine("            { return Result<Unit>.Fail(ex); }");
                    sb.AppendLine("        }");
                    break;
            }

            sb.AppendLine();
        }

        sb.AppendLine("    }");
        if (hasNs) sb.AppendLine("}");

        ctx.AddSource($"AutoResult.{info.ClassName}.g.cs", sb.ToString());
    }

    // ── Helpers ──────────────────────────────────────────────────────────────
    private static bool IsTaskOf(ITypeSymbol type, out ITypeSymbol? typeArg)
    {
        typeArg = null;
        if (type is not INamedTypeSymbol named) return false;

        var def = named.OriginalDefinition.ToDisplayString();
        if (def == "System.Threading.Tasks.Task<TResult>" && named.TypeArguments.Length == 1)
        {
            typeArg = named.TypeArguments[0];
            return true;
        }
        if (def == "System.Threading.Tasks.Task")
            return true;

        return false;
    }
}
