using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using System.Threading;

namespace AutoResult;

[Generator]
public sealed class AutoResultGenerator : IIncrementalGenerator
{
    // ── Types emitted into every consuming project ───────────────────────────
    private const string CoreTypesSource = @"// <auto-generated by AutoResult.Generator/>
#nullable enable
using System;
using System.Threading.Tasks;

namespace AutoResult
{
    /// <summary>Represents the result of an operation that can succeed with a value or fail with an exception.</summary>
    public readonly struct Result<T>
    {
        private readonly T? _value;
        private readonly Exception? _error;

        public bool IsSuccess { get; }
        public bool IsFailure => !IsSuccess;

        public T Value => IsSuccess ? _value! : throw new InvalidOperationException(""Result is a failure. Check IsSuccess before accessing Value."");
        public Exception Error => !IsSuccess ? _error! : throw new InvalidOperationException(""Result is a success. Check IsFailure before accessing Error."");

        private Result(bool isSuccess, T? value, Exception? error)
        {
            IsSuccess = isSuccess;
            _value    = value;
            _error    = error;
        }

        public static Result<T> Ok(T value) => new Result<T>(true, value, null);
        public static Result<T> Fail(Exception error) => new Result<T>(false, default, error ?? throw new ArgumentNullException(nameof(error)));
        public static Result<T> Fail(string message) => Fail(new Exception(message));

        public static implicit operator Result<T>(T value)         => Ok(value);
        public static implicit operator Result<T>(Exception error)  => Fail(error);

        public override string ToString() => IsSuccess ? $""Ok({_value})"" : $""Fail({_error?.Message})"";
    }

    /// <summary>Represents the result of an operation that can succeed with a value or fail with a typed error.</summary>
    public readonly struct Result<T, TError>
    {
        private readonly T?      _value;
        private readonly TError? _error;

        public bool IsSuccess { get; }
        public bool IsFailure => !IsSuccess;

        public T      Value => IsSuccess  ? _value! : throw new InvalidOperationException(""Result is a failure."");
        public TError Error => !IsSuccess ? _error! : throw new InvalidOperationException(""Result is a success."");

        private Result(bool isSuccess, T? value, TError? error)
        {
            IsSuccess = isSuccess;
            _value    = value;
            _error    = error;
        }

        public static Result<T, TError> Ok(T value)       => new Result<T, TError>(true,  value,   default);
        public static Result<T, TError> Fail(TError error) => new Result<T, TError>(false, default, error);

        public static implicit operator Result<T, TError>(T      value) => Ok(value);
        public static implicit operator Result<T, TError>(TError error) => Fail(error);

        public override string ToString() => IsSuccess ? $""Ok({_value})"" : $""Fail({_error})"";
    }

    /// <summary>Represents the absence of a meaningful value (for void-returning methods wrapped by [TryWrap]).</summary>
    public readonly struct Unit : IEquatable<Unit>
    {
        public static readonly Unit Value = default;
        public bool Equals(Unit other) => true;
        public override bool Equals(object? obj) => obj is Unit;
        public override int  GetHashCode() => 0;
        public override string ToString()  => ""()"";
        public static bool operator ==(Unit left, Unit right) => true;
        public static bool operator !=(Unit left, Unit right) => false;
    }

    /// <summary>Extension methods for Result&lt;T&gt; and Result&lt;T, TError&gt;.</summary>
    public static class ResultExtensions
    {
        public static Result<TOut> Map<T, TOut>(
            this Result<T> result, Func<T, TOut> mapper)
            => result.IsSuccess
                ? Result<TOut>.Ok(mapper(result.Value))
                : Result<TOut>.Fail(result.Error);

        public static Result<TOut, TError> Map<T, TOut, TError>(
            this Result<T, TError> result, Func<T, TOut> mapper)
            => result.IsSuccess
                ? Result<TOut, TError>.Ok(mapper(result.Value))
                : Result<TOut, TError>.Fail(result.Error);

        public static Result<TOut> Bind<T, TOut>(
            this Result<T> result, Func<T, Result<TOut>> binder)
            => result.IsSuccess ? binder(result.Value) : Result<TOut>.Fail(result.Error);

        public static Result<TOut, TError> Bind<T, TOut, TError>(
            this Result<T, TError> result, Func<T, Result<TOut, TError>> binder)
            => result.IsSuccess ? binder(result.Value) : Result<TOut, TError>.Fail(result.Error);

        public static TOut Match<T, TOut>(
            this Result<T> result,
            Func<T, TOut> onSuccess,
            Func<Exception, TOut> onFailure)
            => result.IsSuccess ? onSuccess(result.Value) : onFailure(result.Error);

        public static TOut Match<T, TError, TOut>(
            this Result<T, TError> result,
            Func<T, TOut> onSuccess,
            Func<TError, TOut> onFailure)
            => result.IsSuccess ? onSuccess(result.Value) : onFailure(result.Error);

        public static Result<T> OnSuccess<T>(this Result<T> result, Action<T> action)
        {
            if (result.IsSuccess) action(result.Value);
            return result;
        }

        public static Result<T, TError> OnSuccess<T, TError>(
            this Result<T, TError> result, Action<T> action)
        {
            if (result.IsSuccess) action(result.Value);
            return result;
        }

        public static Result<T> OnFailure<T>(this Result<T> result, Action<Exception> action)
        {
            if (result.IsFailure) action(result.Error);
            return result;
        }

        public static Result<T, TError> OnFailure<T, TError>(
            this Result<T, TError> result, Action<TError> action)
        {
            if (result.IsFailure) action(result.Error);
            return result;
        }

        public static async System.Threading.Tasks.Task<Result<TOut>> MapAsync<T, TOut>(
            this System.Threading.Tasks.Task<Result<T>> resultTask, Func<T, TOut> mapper)
        {
            var result = await resultTask.ConfigureAwait(false);
            return result.Map(mapper);
        }

        public static async System.Threading.Tasks.Task<Result<TOut>> BindAsync<T, TOut>(
            this System.Threading.Tasks.Task<Result<T>> resultTask, Func<T, Result<TOut>> binder)
